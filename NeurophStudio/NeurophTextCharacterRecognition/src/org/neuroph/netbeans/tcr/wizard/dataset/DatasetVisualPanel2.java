    private void prepareAllFilters() {
        allFiltersModel = new DefaultListModel();
        allFiltersModel.addElement(new OtsuBinarizeFilter());
        allFiltersModel.addElement(new ZhangSuenThinFilter());
        allFiltersModel.addElement(new GrayscaleFilter());
        allFiltersModel.addElement(new AdaptiveThresholdBinarizeFilter());
        lstAllFilters.setModel(allFiltersModel);
    }

    private void prepareSelectgedFilters() {
       selectedFiltersModel = new DefaultListModel();
       selectedFiltersModel.addElement(new OtsuBinarizeFilter());
       lstSelectedFilters.setModel(selectedFiltersModel);
    }
    
    
