    {
        Rectangle rec = getRect();
        g.setColor(Color.WHITE);
        g.fillRect(0, 0, rec.width, rec.height);
        drawRect(g);

        g.setColor(Color.BLACK);
        g.drawArc(20, 10, 7, 7, 0, 360);
        g.setColor(Color.RED);
        g.fillArc(20, 10, 7, 7, 0, 360);

        g.setColor(Color.BLACK);
        g.drawString("output: 1", 35, 18);

        g.drawArc(20, 30, 7, 7, 0, 360);
        g.setColor(Color.BLUE);
        g.fillArc(20, 30, 7, 7, 0, 360);

        g.setColor(Color.BLACK);
        g.drawString("output: 0", 35, 38);

    }

